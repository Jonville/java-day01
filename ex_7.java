package day1_project;

public class ex_7 {

	public static void main(String[] args) {

		int a = 10;
		int b = 5;

		System.out.println(a > b); // true
		System.out.println(a >= b); // true
		System.out.println(a < b); // false
		System.out.println(a <= b); // false
		System.out.println(a == b); // false
		System.out.println(a != b); // true

	}

}
